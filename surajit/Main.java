package com.surajit;

/* In Java whenever we created any .java file we had to create a class in that file with the same name. And name must have to be on capital case. Whatever code we write we'll write inside this class. Also, this Class had to be PUBLIC, so we can access it from anywhere else. */
public class Main {
    /* as in c++, also in java main function is the entry point in the program. there must have to be a main class, otherwise your code will not work. */
    public static void main(String[] args) {
        System.out.println("hello world!"); /* semicolon (;) is important */
        System.out.println(new int[] {1,2,3});
    }
}